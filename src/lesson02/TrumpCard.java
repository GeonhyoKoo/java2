package lesson02;

public class TrumpCard {
	
	private String shape;
	private int number;
	
	public String getShape() {
		return shape;
	}
	public int getNumber() {
		return number;
	}
	public void setShape(String shape) {
		this.shape = shape;
	}
	public void setNumber(int number) {
		this.number = number;
	}

}
